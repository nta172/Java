package com.vti.frontend;

public class Exercise6 {
    public static void main(String[] args) {
        /* Question 1: Tạo method để in ra các số chẵn nguyên nhỏ hơn 10*/
        for (int i = 1; i <= 10; i++) {
            if ( i % 2 == 0){
                System.out.println(i + " ");
            }
        }

        /* Question 2: Tạo method để in ra các số nguyên dương nhỏ hơn 10 */
        for (int j = 0; j < 10; j++) {
            System.out.println(j + " ");            
        }
    }
}
